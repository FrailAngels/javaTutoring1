public class fourthClassEX3 {
    public static void main(String[] args) {
        int x = 10;
        int y = 20;

        System.out.print("Before Swap: " + x + " ");
        System.out.print(y);

        y = x;
        x = 20; 

        System.out.print(" " + "Before Swap: " + x + " ");
        System.out.print(y);


    }
    
}
